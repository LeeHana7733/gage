package com.hana.gage.domain;

public class CategoryVO {
	private String cateOid				= "";
	private String cateName			= "";
	private String cateType			= "";
	private String cateUpdateDate	= "";
	
	public String getCateOid() {
		return cateOid;
	}
	public void setCateOid(String cateOid) {
		this.cateOid = cateOid;
	}
	public String getCateName() {
		return cateName;
	}
	public void setCateName(String cateName) {
		this.cateName = cateName;
	}
	public String getCateType() {
		return cateType;
	}
	public void setCateType(String cateType) {
		this.cateType = cateType;
	}
	public String getCateUpdateDate() {
		return cateUpdateDate;
	}
	public void setCateUpdateDate(String cateUpdateDate) {
		this.cateUpdateDate = cateUpdateDate;
	}
	
	
}
